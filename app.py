def time_bucket(hour):
    if 4 <= hour < 8: return 'EarlyMorning'
    elif 8 <= hour < 12: return 'Morning'
    elif 12 <= hour < 16: return 'Afternoon'
    elif 16 <= hour < 20: return 'Evening'
    else: return 'Night'


def calculate_duration(dep_hour, dep_minute, arr_hour, arr_minute, next_day=False):
    dep_total = dep_hour * 60 + dep_minute
    arr_total = arr_hour * 60 + arr_minute
    if next_day or arr_total < dep_total:
        arr_total += 24 * 60
    return arr_total - dep_total


def haversine_km(lat1, lon1, lat2, lon2):
    R = 6371
    p1, p2 = np.radians(lat1), np.radians(lat2)
    dp = np.radians(lat2 - lat1)
    dl = np.radians(lon2 - lon1)
    a = np.sin(dp/2)**2 + np.cos(p1) * np.cos(p2) * np.sin(dl/2)**2
    return 2 * R * np.arcsin(np.sqrt(a))


def build_input(airline, source, destination, stops, journey_dt, dep_t, arr_t, next_day):
    duration = calculate_duration(dep_t.hour, dep_t.minute, arr_t.hour, arr_t.minute, next_day)
    return pd.DataFrame([{
        'Airline': airline,
        'Source': source,
        'Destination': destination,
        'Total_Stops': stops,
        'Journey_Day': journey_dt.day,
        'Journey_Month': journey_dt.month,
        'Journey_Weekday': journey_dt.weekday(),
        'Is_Weekend': 1 if journey_dt.weekday() >= 5 else 0,
        'Dep_Hour': dep_t.hour,
        'Dep_Minute': dep_t.minute,
        'Dep_TimeBucket': time_bucket(dep_t.hour),
        'Arrival_Hour': arr_t.hour,
        'Arrival_Minute': arr_t.minute,
        'Arr_TimeBucket': time_bucket(arr_t.hour),
        'Duration_Mins': duration
    }])


def predict_price(row_df):
    log_price = model.predict(row_df)[0]
    return float(np.expm1(log_price))


def predict_for_date_range(airline, source, destination, stops, start_date, num_days, dep_t, arr_t, next_day):
    """Predict prices for the next N days starting from start_date."""
    results = []
    for i in range(num_days):
        d = pd.to_datetime(start_date) + timedelta(days=i)
        try:
            row = build_input(airline, source, destination, stops, d, dep_t, arr_t, next_day)
            price = predict_price(row)
            results.append({
                'date': d.date(),
                'price': price,
                'weekday': WEEKDAY_NAMES[d.weekday()],
                'day_num': d.day,
                'month': d.month_name()[:3]
            })
        except Exception:
            continue
    return pd.DataFrame(results)


def generate_tips(predicted_price, all_airline_prices, journey_dt, dep_time_bucket, current_airline, best_day_info):
    tips = []
    if journey_dt.weekday() >= 5:
        tips.append(("📅", "You're flying on a weekend — weekday flights are sometimes cheaper. Try Tuesday or Wednesday."))
    else:
        tips.append(("📅", "Smart pick! You're flying mid-week, which is often cheaper than weekends."))

    if dep_time_bucket in ['Morning', 'Evening']:
        tips.append(("🌅", f"{dep_time_bucket} flights are peak slots. Early morning (4–8 AM) flights are usually 5–10% cheaper."))
    elif dep_time_bucket == 'EarlyMorning':
        tips.append(("⭐", "Great choice — early morning flights are often the cheapest of the day."))

    cheaper = {a: p for a, p in all_airline_prices.items() if p < predicted_price - 500 and a != current_airline}
    if cheaper:
        best = min(cheaper, key=cheaper.get)
        savings = predicted_price - cheaper[best]
        tips.append(("💰", f"Switch to **{best}** to save approximately ₹{savings:,.0f}."))

    if 'Business' in current_airline or 'Premium' in current_airline:
        tips.append(("💎", "You've selected a premium class — fare reflects business-class pricing."))

    if journey_dt.month in [4, 5, 6]:
        tips.append(("🌞", "You're flying in summer (peak travel season). Booking 3+ weeks ahead helps lock lower prices."))

    if best_day_info:
        savings = predicted_price - best_day_info['price']
        if savings > 300:
            tips.append(("🎯", f"Flying on **{best_day_info['date'].strftime('%a, %b %d')}** could save you ₹{savings:,.0f}."))

    return tips


